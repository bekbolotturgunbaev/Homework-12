public class Parrot {
    int kluv;
    int wings;
    int paws;
    String color;

    public int getKluv() {
        return kluv;
    }

    public void setKluv(int kluv) {
        this.kluv = kluv;
    }

    public int getWings() {
        return wings;
    }

    public void setWings(int wings) {
        this.wings = wings;
    }

    public int getPaws() {
        return paws;
    }

    public void setPaws(int paws) {
        this.paws = paws;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public void parrotOffly() {
        System.out.println("Попугай может летать");
    }
}
